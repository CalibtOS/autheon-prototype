  function getLocale() {
    const stored = localStorage.getItem(LOCALE_KEY);
    if (stored && MESSAGES[stored]) return stored;
    return navigator.language && navigator.language.toLowerCase().startsWith("de") ? "de" : "en";
  }

  let locale = getLocale();
  document.documentElement.lang = locale;

  function subscribe(fn) {
    listeners.add(fn);
    return () => listeners.delete(fn);
  }

  function emit() {
    document.documentElement.lang = locale;
    listeners.forEach((fn) => fn(locale));
  }

  function setLocale(next) {
    if (!MESSAGES[next] || next === locale) return;
    locale = next;
    localStorage.setItem(LOCALE_KEY, locale);
    emit();
  }

  function readKey(obj, key) {
    return key.split(".").reduce((acc, part) => (acc && acc[part] != null ? acc[part] : undefined), obj);
  }

  function t(key, vars) {
    const base = MESSAGES[locale] || MESSAGES[fallbackLocale];
    const fallback = MESSAGES[fallbackLocale];
    let value = readKey(base, key);
    if (value == null) value = readKey(fallback, key);
    if (typeof value !== "string") return key;
    if (!vars) return value;
    return value.replace(/\{(\w+)\}/g, (_, token) => (vars[token] == null ? "" : String(vars[token])));
  }

  window.useI18n = function useI18n() {
    const [, setTick] = React.useState(0);
    React.useEffect(() => subscribe(() => setTick((v) => v + 1)), []);
    return { locale, setLocale, t };
  };

  return { MESSAGES, subscribe, getLocale: () => locale, setLocale, t };
